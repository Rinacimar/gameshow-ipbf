//BOTÃO 01

//Pega a Modal
var modal01 = document.getElementById("myModal01");
//Pega o botão que vai abrir a modal
var btn01 = document.getElementById("01");

btn01.addEventListener("click", open01)
function open01() {
    modal01.style.display = "block";    
}

var button_color01 = document.getElementById("check01")

button_color01.addEventListener("click", trocarCor01)

function trocarCor01() {
    document.getElementById("01").style.backgroundColor = "red";
    modal01.style.display = "none";
}

